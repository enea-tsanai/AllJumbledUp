public class FwStoryPair {

    private String FW, Story;

    public FwStoryPair(String FW, String Story) {
        this.FW = FW;
        this.Story = Story;
    }

    public String getFW() {
        return FW;
    }
    public String getStory() {
        return Story;
    }
}
